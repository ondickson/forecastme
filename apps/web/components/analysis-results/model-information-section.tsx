import { Cpu } from 'lucide-react';

import { Badge } from '@/components/ui/badge';
import { Card, CardContent, CardHeader, CardTitle } from '@/components/ui/card';
import type { ModelInformation } from '@/types/analysis';

interface ModelInformationSectionProps {
  model: ModelInformation;
}

function getModelClassification(model: ModelInformation): string | null {
  const description = `${model.name} ${model.method ?? ''}`.toLowerCase();

  if (
    description.includes('placeholder') ||
    description.includes('contract-validator') ||
    description.includes('contract validator')
  ) {
    return 'Development validation engine';
  }

  if (description.includes('baseline')) {
    return 'Baseline model';
  }

  return null;
}

export function ModelInformationSection({ model }: ModelInformationSectionProps) {
  const classification = getModelClassification(model);

  return (
    <Card>
      <CardHeader className="gap-2">
        <CardTitle as="h3" className="flex items-center gap-2 text-base">
          <Cpu aria-hidden="true" className="size-4 text-primary" />
          Model information
        </CardTitle>
      </CardHeader>

      <CardContent className="space-y-3">
        {classification && <Badge variant="secondary">{classification}</Badge>}

        <dl className="grid gap-3 text-sm sm:grid-cols-2">
          <div className="space-y-1">
            <dt className="text-xs font-medium uppercase tracking-wide text-muted-foreground">
              Model
            </dt>
            <dd className="break-words text-foreground">{model.name}</dd>
          </div>

          <div className="space-y-1">
            <dt className="text-xs font-medium uppercase tracking-wide text-muted-foreground">
              Version
            </dt>
            <dd className="break-words font-mono text-foreground">{model.version}</dd>
          </div>

          <div className="space-y-1 sm:col-span-2">
            <dt className="text-xs font-medium uppercase tracking-wide text-muted-foreground">
              Method or engine
            </dt>
            <dd className="break-words text-foreground">
              {model.method?.trim() || 'Not available'}
            </dd>
          </div>
        </dl>

        {classification === 'Development validation engine' && (
          <p className="text-xs leading-5 text-muted-foreground">
            This result was generated by ForecastMe&apos;s deterministic validation engine to test
            the complete analysis workflow. It is not a trained forecast and should not be used for
            real-world decisions.
          </p>
        )}
      </CardContent>
    </Card>
  );
}
